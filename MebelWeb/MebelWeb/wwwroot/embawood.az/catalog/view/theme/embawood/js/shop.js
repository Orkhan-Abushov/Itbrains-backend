var storeDistances = [];

$(function(){
    navigator.permissions.query({name:'geolocation'}).then(function(result) {
        result.onchange = function () {
            $('#locationModal').modal('hide');
        }

        switch (result.state) {
            case "prompt":
                prompt();
                break;

            case "granted":
                granted();
                break;

            case "denied":
                denied();
                break;

            default:
                return;
        }
    });
})

var rad = function(x) {
    return x * Math.PI / 180;
};

function getPosition() {
    if (navigator.geolocation) {
        navigator.geolocation.getCurrentPosition((position) => {
            const latitude = position.coords.latitude;
            const longitude = position.coords.longitude;
            const R = 6378137; // Earth’s mean radius in meter

            storesInfo.forEach(info => {
                var dLat = rad(info.shop_lat - latitude);
                var dLong = rad(info.shop_lng - longitude);
                var a = Math.sin(dLat / 2) * Math.sin(dLat / 2)
                    + Math.cos(rad(latitude)) * Math.cos(rad(info.shop_lat)) * Math.sin(dLong / 2)
                    * Math.sin(dLong / 2);
                var c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
                var d = R * c;
                storeDistances.push({ ...info, distance: parseInt(d) });
            });

            storeDistances.sort((s1, s2) => s1.distance - s2.distance);
            console.log(storeDistances[0].shop_name)

            const markup = `
                <div class="shop">
                    <p class="shop-name">${ storeDistances[0].shop_name }</p>
                    <span class="shop-address mb-1">${ storeDistances[0].shop_address }</span>
                    <span class="${ storeDistances[0].shop_phone1 ? "shop-phone" : "" }">${ storeDistances[0].shop_phone1 } </span>
                    <span class="${ storeDistances[0].shop_phone2 ? "shop-phone" : "" }">${ storeDistances[0].shop_phone2 } </span>
                    <span class="${ storeDistances[0].shop_phone3 ? "shop-phone" : "" }">${ storeDistances[0].shop_phone3 } </span>
                    <p class="mb-0 ml-2 text-primary">
                        <a href="${storeDistances[0].url}" target="_blank" class="btn-link">Xəritədə bax <span class="ml-2"><i class="icon-arrow-right8"></i></span></a>
                    </p>
                </div>
            `;

            $("#resultModal .modal-body").html(markup);
            $("#resultModal").modal("show");
        });
    } else {
        changeModalText("Sizə ən yaxın mağazanı göstərmək üçün brauzeri yeniləyin.");
    }
}

function prompt() {
    $('#locationModal').modal('show');
    getPosition();
}

function granted() {
    getPosition();
}

